'use strict'

/*
 ***********************************************************
 *                                                         *
 *                    Operador typeof                      *
 *                                                         *
 ***********************************************************
 
    - Permite saber el tipo de dato de una variable.
*/

const numero = 15;
console.log(typeof numero); // number

const cadena = 'Gonzalo';
console.log(typeof cadena); // string

const autenticado = true;
console.log(typeof autenticado); // boolean
